X = float(input("Digite um número: "))
Y = float(input("Digite outro número: "))
Z = float(input("Digite mais um número: "))

if X < Y < Z :
    print("Crecesnte")
elif X > Y > Z:
    print("Decrescente")
elif not  X < Y < Z or not X > Y > Z :
    print("Eles estão misturados!")
    




